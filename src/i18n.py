def get_lang():
    return _CURRENT_LANG


def set_lang(lang):
    global _CURRENT_LANG
    if lang in ("cn", "en"):
        _CURRENT_LANG = lang


def toggle_lang():
    global _CURRENT_LANG
    _CURRENT_LANG = "en" if _CURRENT_LANG == "cn" else "cn"
    return _CURRENT_LANG


def tr(key, **kwargs):
    lang_dict = TR.get(_CURRENT_LANG, TR["cn"])
    text = lang_dict.get(key)
    if text is None:
        text = TR["cn"].get(key, key)
    if kwargs:
        text = text.format(**kwargs)
    return text
